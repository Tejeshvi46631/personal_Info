class UserInfo {
  final String userId; // for edit and delete purpose as 
  final String name;
  final int age;
  final String email;
  final String dob;
  final String gender;
  final String employmentStatus;
  final String address;

  UserInfo({
    required this.userId,
    required this.name,
    required this.age,
    required this.email,
    required this.dob,
    required this.gender,
    required this.employmentStatus,
    required this.address,
  });
}
